import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.neighbors import KNeighborsRegressor
from sklearn.metrics import mean_squared_error, r2_score
import matplotlib.pyplot as plt

"""## **Loading the Data**"""

df = pd.read_csv('/content/Student_performance_data.csv')

df.head(5)

"""## **Data Preprocessing**

### **Data Cleaning (For Null Values)**
"""

print("\nMissing Values:\n", df.isnull().sum())

"""### **Define Features and Target Value**"""

X = df.drop('GPA', axis=1)
y = df['GPA']

"""### **Data Splitting in Train and Test**"""

X_encoded = pd.get_dummies(X, drop_first=True)

X_train, X_test, y_train, y_test = train_test_split(X_encoded, y, test_size=0.2, random_state=42)

"""### **Feature Scaling**"""

scaler = StandardScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)

"""## **Training the Model and Prediction**"""

knn = KNeighborsRegressor(n_neighbors=24)
knn.fit(X_train_scaled, y_train)
y_pred = knn.predict(X_test_scaled)

print("RÂ² Score:", r2_score(y_test, y_pred))
print("MSE:", mean_squared_error(y_test, y_pred))

"""## **Plot Accuracy vs K values**"""

plt.figure(figsize=(8,6))
plt.scatter(y_test, y_pred, color='blue', alpha=0.6)
plt.plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], color='red', linestyle='--')
plt.xlabel('Actual GPA')
plt.ylabel('Predicted GPA')
plt.title('Actual vs Predicted GPA - KNN Regression')
plt.grid(True)
plt.show()